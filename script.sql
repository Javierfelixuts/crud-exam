INNER JOIN #TmpFacturasDet D 
    ON F.Factura = D.Factura
INNER JOIN #TmpArticulos A
    ON D.ItemNmbr = A.ItemNmbr
WHERE LEFT(D.ItemNmbr,1) = 'O'
GROUP BY 
    F.SlprsnId,
    SUBSTRING(D.ItemNmbr, 2, LEN(D.ItemNmbr)),
    A.Modelo, 
    A.Voltaje, 
    A.BTU, 
    A.Tipo;